    private void jLabel3MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseReleased
        int i = 0;
        LabelSalah.setText("");
        if(app.getAdmin() != null){
            boolean stat = false;
            if(tfUsername.getText().isEmpty()){
                LabelSalah.setText("Username Masih Kosong");
            }else{
                do{
                    if(app.login(tfUsername.getText(), tfPassword.getText())){
                        stat = true;
                        PanelLogin.setVisible(false);
                        PanelMenuUtama.setVisible(true);
                        eExit.setVisible(false);  
                        break;
                    }else if(!stat){
                        LabelSalah.setText("Username atau Password Salah");               
                        i++;
                    }
                }while(i != 3);
            }            
        }else{
            LabelSalah.setText("Silahkan Daftar dulu");
        }
    }//GEN-LAST:event_jLabel3MouseReleased

