    private void btnCariTMakanPMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCariTMakanPMouseReleased
        boolean stat = false;
        if(!tfCariTempatMakanP.getText().isEmpty()){
            TempatMakan[] t = app.getTempat();
            for(int i = 0; i < app.getNTempat(); i++){
                if(t[i].getNama().toLowerCase().equals(tfCariTempatMakanP.getText().toLowerCase())){
                    stat = true;
                    reset();
                    LbNamaMenuPatungan.setText(t[i].getNama());
                    LbNamaMenuPatunganA.setText(t[i].getNama());
                    LbSalahCariMakananP.setText("");
                    ListMenuMakanGrup.setListData(t[i].getListMakanan());
                    ListMenuMakanAnggota.setListData(t[i].getListMakanan());
                    btnBackPG.setVisible(true);
                    LbSalahCariMakananP.setText("");
                    setView(false,false,false,false,false,false,false,false,false,false,false,false,false,false,false,false,false,false,false,false,true,false,false);
                }
            }
            if(!stat){
                LbSalahCariMakananP.setText("Nama tempat makan tidak ditemukan");
            }             
        }else{
            LbSalahCariMakananP.setText("Silahkan masukkan nama tempat makan");
        }
    }//GEN-LAST:event_btnCariTMakanPMouseReleased

