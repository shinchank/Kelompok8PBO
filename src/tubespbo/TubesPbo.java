    private void btnBuatMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuatMouseReleased
        boolean stat = false;
        if(tfNamaGrup.getText().isEmpty()){
            LabelSalahBuatGrup.setText("Nama masih kosong");
        }else{
            if(app.getNGrup() != app.getMaxGrup()){
                if(app.getNGrup() != 0){
                    for(int i = 0; i < app.getNGrup(); i++){
                        if(app.cariGrup(tfNamaGrup.getText()) == -1){
                            stat = true;
                            app.buatGrup(tfNamaGrup.getText());
                            LabelSalahBuatGrup.setText("");
                            btnBackMouseReleased(evt);
                            break;
                        }
                    }                    
                }else{
                    app.buatGrup(tfNamaGrup.getText());
                    LabelSalahBuatGrup.setText("");
                    btnBackMouseReleased(evt);                 
                }
                if(!stat){
                    LabelSalahBuatGrup.setText("Nama Grup telah digunakan");
                }
            }else{
                LabelSalahBuatGrup.setText("Grup Sudah Penuh");
            }
        }
        tfNamaGrup.setText("");
    }//GEN-LAST:event_btnBuatMouseReleased

